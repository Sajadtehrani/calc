# ماشین حساب ساده به زبان پایتون

def جمع(a, b):
    return a + b

def تفریق(a, b):
    return a - b

def ضرب(a, b):
    return a * b

def تقسیم(a, b):
    if b != 0:
        return a / b
    else:
        return "تقسیم بر صفر امکان‌پذیر نیست"

print("ماشین حساب ساده")
print("عملیات‌های موجود:")
print("1. جمع")
print("2. تفریق")
print("3. ضرب")
print("4. تقسیم")

while True:
    # انتخاب عملیات
    انتخاب = input("یک عملیات را انتخاب کنید (1/2/3/4): ")

    # بررسی اینکه آیا انتخاب معتبر است
    if انتخاب in ('1', '2', '3', '4'):
        عدد_اول = float(input("عدد اول را وارد کنید: "))
        عدد_دوم = float(input("عدد دوم را وارد کنید: "))

        if انتخاب == '1':
            print("نتیجه:", جمع(عدد_اول, عدد_دوم))

        elif انتخاب == '2':
            print("نتیجه:", تفریق(عدد_اول, عدد_دوم))

        elif انتخاب == '3':
            print("نتیجه:", ضرب(عدد_اول, عدد_دوم))

        elif انتخاب == '4':
            print("نتیجه:", تقسیم(عدد_اول, عدد_دوم))
    else:
        print("ورودی نامعتبر است. لطفاً دوباره تلاش کنید.")
    
    ادامه = input("آیا می‌خواهید عملیات دیگری انجام دهید؟ (بله/خیر): ")
    if ادامه.lower() != 'بله':
        break
